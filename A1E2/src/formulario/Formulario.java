    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(tfId.getText());
        String nombre = tfNombre.getText();
        String autor = tfAutor.getText();
        String descripcion = tfDescrip.getText();
        double precio = Double.parseDouble(tfPrecio.getText());
        
        if (i < max) {
            Libro lib = new Libro(nombre, autor, descripcion, id, precio);
            libros[i] = lib;
            i++;
            limpiar();
            JOptionPane.showMessageDialog(this, "Registro guardado");
            mostrar_libros();
            
        } else {
            JOptionPane.showMessageDialog(this, "No se puede agregar mas libros", "Error", JOptionPane.WARNING_MESSAGE);
        }     
        
    }//GEN-LAST:event_btnRegistrarActionPerformed
    private void mostrar_libros() {
        String reg = "";
        for (int j = 0; j < i; j++){
            reg += libros[j].toString();
        }
        taRegistros.setText(reg);
    }
